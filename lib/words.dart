// ==========================================
// MODELO DE DATOS (CATEGORÍA)
// ==========================================

class Category {
  final String id;
  final String name;
  final String icon;
  final List<String> words;
  final Color color;

  const Category({
    required this.id,
    required this.name,
    required this.icon,
    required this.words,
    required this.color,
  });

  Map<String, dynamic> toMap() {
    return {
      'id': id,
      'name': name,
      'icon': icon,
      'words': words,
      'colorValue': color.value,
    };
  }

  factory Category.fromMap(Map<String, dynamic> map) {
    return Category(
      id: map['id'],
      name: map['name'],
      icon: map['icon'],
      words: List<String>.from(map['words']),
      color: Color(map['colorValue']),
    );
  }
}

// ==========================================
// CONTROLADOR DE IDIOMAS PARA LAS CARTAS
// ==========================================

List<Category> getGameCategories(String langCode) {
  switch (langCode) {
    case 'en':
      return _categoriesEn;
    case 'ru':
      return _categoriesRu;
    case 'pt':
      return _categoriesPt;
    case 'es':
    default:
      return _categoriesEs;
  }
}

